let x = 10
let y = 3
console.log(`x + y ${x + y}`);
console.log(`x - y ${x - y}`);
console.log(`x * y ${x * y}`);
console.log(`x / y ${x / y}`);
console.log(`x % y ${x % y}`);
console.log(`x ** y ${x ** y}`);

// Increment

console.log(`x++ ${x++}`);
console.log(`++x ${++x}`);

// Decrement

console.log(`x-- ${x--}`);
console.log(`--x ${--x}`);